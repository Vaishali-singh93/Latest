package onboardlearning.learning1;

public class PizzaToppings {
    private int tomatoToppingsCost;
    private int cornToppingsCost;
    private int mushroomToppingsCost;
    private int onionToppingsCost;

    public int getTomatoToppingsCost() {
        return tomatoToppingsCost;
    }

    public void setTomatoToppingsCost(int tomatoToppingsCost) {
        this.tomatoToppingsCost = tomatoToppingsCost;
    }

    public int getCornToppingsCost() {
        return cornToppingsCost;
    }

    public void setCornToppingsCost(int cornToppingsCost) {
        this.cornToppingsCost = cornToppingsCost;
    }

    public int getMushroomToppingsCost() {
        return mushroomToppingsCost;
    }

    public void setMushroomToppingsCost(int mushroomToppingsCost) {
        this.mushroomToppingsCost = mushroomToppingsCost;
    }

    public int getOnionToppingsCost() {
        return onionToppingsCost;
    }

    public void setOnionToppingsCost(int onionToppingsCost) {
        this.onionToppingsCost = onionToppingsCost;
    }

    public int getBlackOlivesToppingsCost() {
        return BlackOlivesToppingsCost;
    }

    public void setBlackOlivesToppingsCost(int blackOlivesToppingsCost) {
        BlackOlivesToppingsCost = blackOlivesToppingsCost;
    }

    public int getJalapenoToppingsCost() {
        return JalapenoToppingsCost;
    }

    public void setJalapenoToppingsCost(int jalapenoToppingsCost) {
        JalapenoToppingsCost = jalapenoToppingsCost;
    }

    private int BlackOlivesToppingsCost;
    private int JalapenoToppingsCost;
  
}
